    { \
        double ret_val = 0.0; \
        if(t < static_cast<double>(B1)) \
        { \
            ret_val = (static_cast<double>(B2)/static_cast<double>(B1)) * t; \
        } \
        else \
        { \
            ret_val = static_cast<double>(B2) * exp((-static_cast<double>(B3)) * pow((t - static_cast<double>(B1)), static_cast<double>(B4))); \
        } \
        return ret_val * (1.0 / static_cast<double>(B2)); \
    }


